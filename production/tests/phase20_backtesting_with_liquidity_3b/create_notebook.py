                "    sharpe_ratio = (annual_return - risk_free_rate) / annual_vol if annual_vol > 0 else 0\n",
                "    \n",
                "    cumulative = (1 + returns).cumprod()\n",
                "    drawdown = (cumulative / cumulative.cummax() - 1)\n",
